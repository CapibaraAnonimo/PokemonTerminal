package pokemonTerminalModel;

public class Ataque {

	int mv1;
	int mv2;
	int mv3;
	int mv4;
	
	
	public Ataque(int mv1, int mv2, int mv3, int mv4) {
		this.mv1 = mv1;
		this.mv2 = mv2;
		this.mv3 = mv3;
		this.mv4 = mv4;
	}


	public int getMv1() {
		return mv1;
	}


	public void setMv1(int mv1) {
		this.mv1 = mv1;
	}


	public int getMv2() {
		return mv2;
	}


	public void setMv2(int mv2) {
		this.mv2 = mv2;
	}


	public int getMv3() {
		return mv3;
	}


	public void setMv3(int mv3) {
		this.mv3 = mv3;
	}


	public int getMv4() {
		return mv4;
	}


	public void setMv4(int mv4) {
		this.mv4 = mv4;
	}
	
	
	
	
}
