package pokemonTerminalModel;

public class Objetos {

	int precio;
	int curacion;
	int revivir;
	int ataque;
	int ataqueEspecial;
	int defensa;
	int defensaEspecial;
	int velocidad;
	int precision;
	
	
	public Objetos(int precio, int curacion, int revivir, int ataque, int ataqueEspecial, int defensa,
			int defensaEspecial, int velocidad, int precision) {
		super();
		this.precio = precio;
		this.curacion = curacion;
		this.revivir = revivir;
		this.ataque = ataque;
		this.ataqueEspecial = ataqueEspecial;
		this.defensa = defensa;
		this.defensaEspecial = defensaEspecial;
		this.velocidad = velocidad;
		this.precision = precision;
	}


	public int getPrecio() {
		return precio;
	}


	public void setPrecio(int precio) {
		this.precio = precio;
	}


	public int getCuracion() {
		return curacion;
	}


	public void setCuracion(int curacion) {
		this.curacion = curacion;
	}


	public int getRevivir() {
		return revivir;
	}


	public void setRevivir(int revivir) {
		this.revivir = revivir;
	}


	public int getAtaque() {
		return ataque;
	}


	public void setAtaque(int ataque) {
		this.ataque = ataque;
	}


	public int getAtaqueEspecial() {
		return ataqueEspecial;
	}


	public void setAtaqueEspecial(int ataqueEspecial) {
		this.ataqueEspecial = ataqueEspecial;
	}


	public int getDefensa() {
		return defensa;
	}


	public void setDefensa(int defensa) {
		this.defensa = defensa;
	}


	public int getDefensaEspecial() {
		return defensaEspecial;
	}


	public void setDefensaEspecial(int defensaEspecial) {
		this.defensaEspecial = defensaEspecial;
	}


	public int getVelocidad() {
		return velocidad;
	}


	public void setVelocidad(int velocidad) {
		this.velocidad = velocidad;
	}


	public int getPrecision() {
		return precision;
	}


	public void setPrecision(int precision) {
		this.precision = precision;
	}
	
	
	
	
	
}
